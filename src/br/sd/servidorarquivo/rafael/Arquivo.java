package br.sd.servidorarquivo.rafael;

import java.io.Serializable;

/**
 *
 * @author rafael
 */
public class Arquivo implements Serializable{

    public Arquivo(){

    }
    
    public Arquivo(AtributoArquivo atributos, byte[] dados) {
        this.atributos = atributos;
        this.dados = dados;
    }

    public AtributoArquivo getAtributos() {
        return atributos;
    }

    public void setAtributos(AtributoArquivo atributos) {
        this.atributos = atributos;
    }

    public byte[] getDados() {
        return dados;
    }

    public void setDados(byte[] dados) {
        this.dados = dados;
    }
    private AtributoArquivo atributos;
    private byte[] dados;
}
